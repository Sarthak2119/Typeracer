    public Resultmulti(int playerno,String use,Statement stm,int pl[],int avg,int best,int acc,int time,int score) {
        initComponents();     
        username=use;
        stmt=stm;
        averagespeed.setText(""+avg+"(CPM)");
        bestspeed.setText(""+best+"(CPS)");
        accuracy.setText(""+acc+"%");
        int min,sec;
        min=time/60;
        sec=time%60;
        timeelapsed.setText(min+":"+sec);
        effectivescore.setText(""+score);
        table=jTable1;
        table.setRowHeight(30);
        table.setRowMargin(5);
        model.addColumn("PLAYER");
        model.addColumn("SCORE");
        int temp;
        String temp2;
        String [] str=new String[2];
        String [] players=new String[4];
        players[0]="PLAYER1";
        players[1]="PLAYER2";
        players[2]="PLAYER3";
        players[3]="PLAYER4";
        for(int i=0;i<3;i++)
        {
            for(int j=0;j<4-i-1;j++)
            {
                if(pl[j]<pl[j+1])
                {
                    temp=pl[j];
                    pl[j]=pl[j+1];
                    pl[j+1]=temp;
                    temp2=players[j];
                    players[j]=players[j+1];
                    players[j+1]=temp2;
                }
            }
        }
        for(int i=0;i<4;i++)
        {
            str[0]=players[i];
            str[1]=""+pl[i];
            model.addRow(str);
        }
        
        String sql="SELECT * FROM users where username = '"+username+"' ";
        try {
            ResultSet result=stmt.executeQuery(sql);
            int bestsp,averag,won,noraces;
            if(result.next())
            {
                bestsp=Integer.parseInt(result.getString("bestspeed"));
                averag=Integer.parseInt(result.getString("avgspeed"));
                won=Integer.parseInt(result.getString("raceswon"));
                noraces=Integer.parseInt(result.getString("racesplayed"));
                if(playerno==1&&players[0].equals("PLAYER1"))
                    won++;
                else if(playerno==2&&players[0].equals("PLAYER2"))
                    won++;
                else if(playerno==3&&players[0].equals("PLAYER3"))
                    won++;
                else if(playerno==4&&players[0].equals("PLAYER4"))
                    won++;
                if(bestsp<avg)
                    bestsp=avg;
                averag=averag*noraces;
                noraces++;
                averag=(averag+avg)/noraces;
                sql="UPDATE users SET bestspeed='"+bestsp+"', avgspeed='"+averag+"', lastspeed='"+avg+"', raceswon='"+won+"', racesplayed='"+noraces+"' WHERE username='"+username+"'";                           
                stmt.executeUpdate(sql);
            }
        } catch (SQLException ex) {
            Logger.getLogger(Resultmulti.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        table.setEnabled(false);
        table.setModel(model);
    }

